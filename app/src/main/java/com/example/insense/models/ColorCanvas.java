package com.example.insense.models;

public class ColorCanvas {
    int al,red,green,blue;

    public ColorCanvas(int al, int red,int green,int blue){
        this.al=al;
        this.red=red;
        this.green=green;
        this.blue=blue;
    }

    public int getAl() {
        return al;
    }

    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBlue() {
        return blue;
    }
}
